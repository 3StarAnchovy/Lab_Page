package bbs;

public class CodeBbs {
	private int CodeID;
	private String CodeTitle;
	private String CodeContent;
	private String CodeDate;
	private String CodeManager;
	private String CodeManagerName;
	private int CodeLike;
	private String CodeType;
	private int CodeAvailable;
	
	public int getCodeID() {
		return CodeID;
	}
	public void setCodeID(int codeID) {
		CodeID = codeID;
	}
	public String getCodeTitle() {
		return CodeTitle;
	}
	public void setCodeTitle(String codeTitle) {
		CodeTitle = codeTitle;
	}
	public String getCodeContent() {
		return CodeContent;
	}
	public void setCodeContent(String codeContent) {
		CodeContent = codeContent;
	}
	public String getCodeDate() {
		return CodeDate;
	}
	public void setCodeDate(String codeDate) {
		CodeDate = codeDate;
	}
	public String getCodeManager() {
		return CodeManager;
	}
	public void setCodeManager(String codeManager) {
		CodeManager = codeManager;
	}
	public String getCodeManagerName() {
		return CodeManagerName;
	}
	public void setCodeManagerName(String codeManagerName) {
		CodeManagerName = codeManagerName;
	}
	public int getCodeLike() {
		return CodeLike;
	}
	public void setCodeLike(int codeLike) {
		CodeLike = codeLike;
	}
	public String getCodeType() {
		return CodeType;
	}
	public void setCodeType(String codeType) {
		CodeType = codeType;
	}
	public int getCodeAvailable() {
		return CodeAvailable;
	}
	public void setCodeAvailable(int codeAvailable) {
		CodeAvailable = codeAvailable;
	}
	
}
