package bbs;

public class NoticeBbs {
	private int NoticeID;
	private String NoticeTitle;
	private String NoticeContent;
	private String NoticeDate;
	private String NoticeManager;
	private String NoticeManagerName;
	private String Noticefilesrc;
	private int Noticehit;
	private int NoticeAvailable;
	
	public int getNoticeID() {
		return NoticeID;
	}
	public void setNoticeID(int noticeID) {
		NoticeID = noticeID;
	}
	public String getNoticeTitle() {
		return NoticeTitle;
	}
	public void setNoticeTitle(String noticeTitle) {
		NoticeTitle = noticeTitle;
	}
	public String getNoticeContent() {
		return NoticeContent;
	}
	public void setNoticeContent(String noticeContent) {
		NoticeContent = noticeContent;
	}
	public String getNoticeDate() {
		return NoticeDate;
	}
	public void setNoticeDate(String noticeDate) {
		NoticeDate = noticeDate;
	}
	public String getNoticeManager() {
		return NoticeManager;
	}
	public void setNoticeManager(String noticeManager) {
		NoticeManager = noticeManager;
	}
	public String getNoticefilesrc() {
		return Noticefilesrc;
	}
	public void setNoticefilesrc(String noticefilesrc) {
		Noticefilesrc = noticefilesrc;
	}
	public int getNoticehit() {
		return Noticehit;
	}
	public void setNoticehit(int noticehit) {
		Noticehit = noticehit;
	}
	public int getNoticeAvailable() {
		return NoticeAvailable;
	}
	public void setNoticeAvailable(int noticeAvailable) {
		NoticeAvailable = noticeAvailable;
	}
	public String getNoticeManagerName() {
		return NoticeManagerName;
	}
	public void setNoticeManagerName(String noticeManagerName) {
		NoticeManagerName = noticeManagerName;
	}
}
