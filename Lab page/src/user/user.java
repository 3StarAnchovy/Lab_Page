package user;

public class user {
	private String userID;
	private String userPassword;
	private String userName;
	private String userprofileName;
	private String userprofileRealName;
	private int userPower;

	public String getUserID() {
		return userID;
	}

	public void setUserID(String userID) {
		this.userID = userID;
	}

	public String getUserPassword() {
		return userPassword;
	}

	public void setUserPassword(String userPassword) {
		this.userPassword = userPassword;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getUserprofileName() {
		return userprofileName;
	}

	public void setUserprofileName(String userprofileName) {
		this.userprofileName = userprofileName;
	}

	public String getUserprofileRealName() {
		return userprofileRealName;
	}

	public void setUserprofileRealName(String userprofileRealName) {
		this.userprofileRealName = userprofileRealName;
	}

	public int getUserPower() {
		return userPower;
	}

	public void setUserPower(int userPower) {
		this.userPower = userPower;
	}

}
